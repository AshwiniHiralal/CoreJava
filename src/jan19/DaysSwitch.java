package jan19;

public class DaysSwitch
{



		public static void main(String[]args) 
		{
			
			int day=1;
			switch(day)
			{
			  case 1:
			  System.out.println("First day of the week is the sunday");
			  break;
			
			  case 2:
				  System.out.println("Second day of the week is the monday");
				  break;
				
			  case 3:
				  System.out.println("Third day of the week is the tuesday");
				  break;
				
			  case 4:
				  System.out.println("Fourth day of the week is the wensday");
				  break;
				
			  case 5:
				  System.out.println("Fifth day of the week is the thursday");
				  break;
				
			  case 6:
				  System.out.println("sixth day of the week is the friday");
				  break;
				
			  case 7:
				  System.out.println("Seventh day of the week is the saturdayday");
				  break;
				
			default:
				System.out.println("Invalid input");
				
			
			
			
			}
			
			
		}
		
}

